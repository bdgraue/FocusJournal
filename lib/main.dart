import 'package:flutter/material.dart';
import 'screens/authentication_screen.dart';
import 'screens/password_setup_screen.dart';
import 'services/auth_service.dart';

void main() {
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Focus Journal',
      theme: ThemeData(
        colorScheme: ColorScheme.fromSeed(seedColor: Colors.deepPurple),
        useMaterial3: true,
      ),
      home: const AuthenticationWrapper(),
    );
  }
}

class AuthenticationWrapper extends StatefulWidget {
  const AuthenticationWrapper({super.key});

  @override
  State<AuthenticationWrapper> createState() => _AuthenticationWrapperState();
}

class _AuthenticationWrapperState extends State<AuthenticationWrapper> {
  final _authService = AuthService();
  bool? _isAuthSetup;
  bool _isAuthenticated = false;

  @override
  void initState() {
    super.initState();
    _checkAuthSetup();
  }

  Future<void> _checkAuthSetup() async {
    final isSetup = await _authService.isAuthenticationSetup();
    setState(() {
      _isAuthSetup = isSetup;
    });
  }

  void _onAuthenticationSuccess() {
    setState(() {
      _isAuthenticated = true;
    });
  }

  void _onSetupComplete() {
    setState(() {
      _isAuthSetup = true;
    });
  }

  @override
  Widget build(BuildContext context) {
    if (_isAuthSetup == null) {
      return const Scaffold(
        body: Center(
          child: CircularProgressIndicator(),
        ),
      );
    }

    if (!_isAuthSetup!) {
      return PasswordSetupScreen(onSetupComplete: _onSetupComplete);
    }

    if (!_isAuthenticated) {
      return AuthenticationScreen(onAuthenticationSuccess: _onAuthenticationSuccess);
    }

    return const JournalHomePage();
  }
}

class JournalHomePage extends StatelessWidget {
  const JournalHomePage({super.key});

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

